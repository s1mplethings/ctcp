QUIZ_APP = textwrap.dedent("""
from __future__ import annotations

import argparse, json
from pathlib import Path


def load_questions(path: Path) -> list[dict[str, object]]:
    data = json.loads(Path(path).read_text(encoding="utf-8"))
    return data["questions"] if isinstance(data, dict) else data


def score_answers(questions: list[dict[str, object]], answers: list[str]) -> dict[str, object]:
    correct = 0
    details = []
    for index, question in enumerate(questions):
        expected = str(question.get("answer", "")).strip().upper()
        actual = answers[index].strip().upper() if index < len(answers) else ""
        ok = actual == expected
        correct += int(ok)
        details.append({"question": index + 1, "answer": actual, "expected": expected, "correct": ok})
    return {"score": correct, "total": len(questions), "details": details}


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--questions", required=True)
    parser.add_argument("--test-mode", action="store_true")
    parser.add_argument("--answers", default="")
    args = parser.parse_args(argv)
    questions = load_questions(Path(args.questions))
    if args.test_mode:
        print(json.dumps(score_answers(questions, [x for x in args.answers.split(",") if x]), sort_keys=True))
        return 0
    answers = []
    for question in questions:
        print(question["prompt"])
        for key, value in dict(question.get("choices", {})).items():
            print(f"{key}. {value}")
        answers.append(input("Answer: "))
    result = score_answers(questions, answers)
    print(f"Score: {result['score']}/{result['total']}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
""").lstrip()

