    common = standard_support_files(
        project_id=project_id,
        workflow_doc_rel=workflow_doc_rel,
        provenance=provenance,
        project_archetype="cli_toolkit",
        core_notes=f"# Core Runtime Notes\n\n- project_id: {project_id}\n- runtime: non_web_local\n- ordinary_mainline: new-run/status/advance/source_generation\n",
        workflow_notes=f"# {project_id} Workflow\n\n- Generate local non-web source files.\n- Run generated unittest suite.\n- Validate CLI/package behavior with deterministic local inputs.\n",
    )
    if project_id == "csv_expense_analyzer":
        files = {"README.md": _readme(project_id, goal_text, "python expense_analyzer.py --input sample_expenses.csv --output report.json"), "expense_analyzer.py": CSV_APP, "tests/test_expense_analyzer.py": CSV_TESTS, "sample_expenses.csv": "date,category,amount\n2026-01-05,Food,12.50\n2026-01-09,Travel,20\n2026-02-01,Food,7.50\n", "tests/__init__.py": "# generated test package\n", **common}
    elif project_id == "log_analyzer_cli":
        files = {"README.md": _readme(project_id, goal_text, "python log_analyzer.py --input sample.log --output summary.json"), "log_analyzer.py": LOG_APP, "tests/test_log_analyzer.py": LOG_TESTS, "sample.log": "INFO started\nWARN slow response\nERROR disk full\nERROR disk full\n", "tests/__init__.py": "# generated test package\n", **common}
