def detect_non_web_project(goal: str) -> str:
    haystack = str(goal or "").lower()
    for project_id, spec in PROJECTS.items():
        if any(token in haystack for token in spec["keywords"]) and all(token in haystack for token in spec["required"]):
            return project_id
    return ""


