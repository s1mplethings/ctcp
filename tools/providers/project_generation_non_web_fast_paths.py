        files = {"README.md": _readme(project_id, goal_text, "python -m unittest discover -v"), "text_utils/__init__.py": TEXT_INIT, "text_utils/core.py": TEXT_CORE, "tests/test_text_utils.py": TEXT_TESTS, "tests/__init__.py": "# generated test package\n", **common}
