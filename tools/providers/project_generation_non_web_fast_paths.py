LOG_APP = textwrap.dedent("""
from __future__ import annotations

import argparse, json, re
from collections import Counter
from pathlib import Path

LEVELS = ("INFO", "WARN", "ERROR")


def analyze_log(path: Path) -> dict[str, object]:
    counts = Counter({level: 0 for level in LEVELS})
    errors = Counter()
    pattern = re.compile(r"\\b(INFO|WARN|ERROR)\\b[:\\s-]*(.*)")
    for line in Path(path).read_text(encoding="utf-8").splitlines():
        match = pattern.search(line)
        if not match:
            continue
        level, message = match.group(1), match.group(2).strip()
        counts[level] += 1
        if level == "ERROR" and message:
            errors[message] += 1
    return {"level_counts": dict(counts), "top_errors": [{"message": msg, "count": count} for msg, count in errors.most_common(5)]}


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--input", required=True)
    parser.add_argument("--output", required=True)
    args = parser.parse_args(argv)
    Path(args.output).write_text(json.dumps(analyze_log(Path(args.input)), indent=2) + "\\n", encoding="utf-8")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
""").lstrip()

