TEXT_CORE = textwrap.dedent("""
from __future__ import annotations

import re
from collections import Counter

STOPWORDS = {"a", "an", "and", "for", "in", "of", "or", "the", "to"}


def normalize_whitespace(text: str) -> str:
    return re.sub(r"\\s+", " ", str(text or "")).strip()


def slugify(text: str) -> str:
    cleaned = re.sub(r"[^a-z0-9]+", "-", normalize_whitespace(text).lower())
    return cleaned.strip("-")


def word_count(text: str) -> int:
    return len(re.findall(r"[A-Za-z0-9']+", str(text or "")))


def extract_keywords(text: str, limit: int = 5) -> list[str]:
    words = [w.lower() for w in re.findall(r"[A-Za-z0-9']+", str(text or "")) if w.lower() not in STOPWORDS]
    return [word for word, _count in Counter(words).most_common(limit)]
""").lstrip()
