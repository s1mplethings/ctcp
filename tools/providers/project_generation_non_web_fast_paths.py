from __future__ import annotations

import argparse, csv, json
from collections import defaultdict
from pathlib import Path


def analyze_expenses(path: Path) -> dict[str, object]:
    category = defaultdict(float)
    monthly = defaultdict(float)
    rows = 0
    with Path(path).open(newline="", encoding="utf-8") as handle:
        for row in csv.DictReader(handle):
            rows += 1
            amount = float(row.get("amount", "0") or 0)
            category[str(row.get("category", "uncategorized"))] += amount
            month = str(row.get("date", ""))[:7] or "unknown"
            monthly[month] += amount
    return {"row_count": rows, "category_totals": dict(category), "monthly_totals": dict(monthly)}


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--input", required=True)
    parser.add_argument("--output", required=True)
    args = parser.parse_args(argv)
    report = analyze_expenses(Path(args.input))
    Path(args.output).write_text(json.dumps(report, indent=2, sort_keys=True) + "\\n", encoding="utf-8")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
