from __future__ import annotations

import json
import re
from datetime import datetime, timezone
from pathlib import Path


class NotesStore:
    def __init__(self, root: Path) -> None:
        self.root = Path(root)
        self.root.mkdir(parents=True, exist_ok=True)
        self.index_path = self.root / "index.json"
        if not self.index_path.exists():
            self.index_path.write_text("[]\\n", encoding="utf-8")

    def _index(self) -> list[dict[str, object]]:
        try:
            rows = json.loads(self.index_path.read_text(encoding="utf-8"))
        except Exception:
            rows = []
        return rows if isinstance(rows, list) else []

    def _save(self, rows: list[dict[str, object]]) -> None:
        self.index_path.write_text(json.dumps(rows, ensure_ascii=False, indent=2) + "\\n", encoding="utf-8")

    def _slug(self, title: str) -> str:
        return re.sub(r"[^a-z0-9]+", "-", title.lower()).strip("-") or "note"

    def create(self, title: str, markdown: str) -> dict[str, object]:
        rows = self._index()
        note_id = str(len(rows) + 1)
        now = datetime.now(timezone.utc).isoformat()
        path = self.root / f"{note_id}-{self._slug(title)}.md"
        path.write_text(markdown, encoding="utf-8")
        row = {"id": note_id, "title": title, "path": path.name, "created_at": now, "updated_at": now}
        rows.append(row)
        self._save(rows)
        return self.get(note_id) or row

    def list(self) -> list[dict[str, object]]:
        return [self.get(str(row.get("id"))) or row for row in self._index()]

    def get(self, note_id: str) -> dict[str, object] | None:
        for row in self._index():
            if str(row.get("id")) == str(note_id):
                path = self.root / str(row.get("path"))
                out = dict(row)
                out["markdown"] = path.read_text(encoding="utf-8") if path.exists() else ""
                return out
        return None

    def update(self, note_id: str, fields: dict[str, object]) -> dict[str, object] | None:
        rows = self._index()
        for row in rows:
            if str(row.get("id")) == str(note_id):
                if "title" in fields:
                    row["title"] = str(fields["title"])
                if "markdown" in fields:
                    (self.root / str(row["path"])).write_text(str(fields["markdown"]), encoding="utf-8")
                row["updated_at"] = datetime.now(timezone.utc).isoformat()
                self._save(rows)
                return self.get(note_id)
        return None

    def delete(self, note_id: str) -> bool:
        rows = self._index()
        kept = []
        deleted = False
        for row in rows:
            if str(row.get("id")) == str(note_id):
                path = self.root / str(row.get("path"))
                if path.exists():
                    path.unlink()
                deleted = True
            else:
                kept.append(row)
        self._save(kept)
        return deleted

    def search(self, query: str) -> list[dict[str, object]]:
        q = query.lower()
        return [row for row in self.list() if q in str(row.get("title", "")).lower() or q in str(row.get("markdown", "")).lower()]
