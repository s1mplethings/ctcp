class AuthHandler(BaseHTTPRequestHandler):
    store: AuthStore

    def log_message(self, format: str, *args: object) -> None:
        return

    def do_POST(self) -> None:
        parts = split_path(self.path)
        payload = read_json(self)
        if parts == ["register"]:
            try:
                user = self.store.register(str(payload.get("username", "")).strip(), str(payload.get("password", "")))
            except sqlite3.IntegrityError:
                json_response(self, 409, {"error": "username_exists"})
                return
            json_response(self, 201, {"user": user})
        elif parts == ["login"]:
            token = self.store.login(str(payload.get("username", "")).strip(), str(payload.get("password", "")))
            json_response(self, 200 if token else 401, {"token": token} if token else {"error": "invalid_credentials"})
        elif parts == ["logout"]:
            token = bearer(self)
            json_response(self, 200, {"logged_out": self.store.logout(token)})
        else:
            json_response(self, 404, {"error": "not_found"})

    def do_GET(self) -> None:
        parts = split_path(self.path)
        if parts == ["status"]:
            json_response(self, 200, {"status": "ok", "service": "simple_auth_api"})
        elif parts == ["me"]:
            user = self.store.user_for_token(bearer(self))
            json_response(self, 200 if user else 401, {"user": user} if user else {"error": "unauthorized"})
        else:
            json_response(self, 404, {"error": "not_found"})


def create_server(host: str, port: int, db_path: Path) -> ThreadingHTTPServer:
    AuthHandler.store = AuthStore(db_path)
    return ThreadingHTTPServer((host, port), AuthHandler)


